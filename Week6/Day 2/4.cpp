#include <iostream>
using namespace std;

int main(){

    int n, cnt = 0; cin >> n;

    for(int i = 0; i < n; i++){
        int num; cin >> num;
        while(num > 0){
            if(num % 10 == 0) cnt++;
            num /= 10;
        }
    }

    cout << cnt;

    return 0;
}

// Input:
// 5
// 100 10 101 11 50

// 100
// 100 % 10 = 0, cnt = 1, 100 / 10 = 10;
// 10 % 10 = 0, cnt = 2, 10 / 10 = 1;
