#include <iostream>
using namespace std;

int main(){

    int n; cin >> n;
    int right = n - 1, left = n - 1;
    char c = 'A';

    for(int x = 0; x < n; x++){
        for(int y = 0; y < n * 2 - 1; y++){
            // cout << "[" << x << ", " << y << "] ";
            if(c == 'Z' + 1) c = 'A';
            if(n % 2 == 1 and y >= left and y <= right and ((x % 2 == 0 and y % 2 == 0) or (x % 2 == 1 and y % 2 == 1))) cout << c++;
            else if(n % 2 == 0 and y >= left and y <= right and ((x % 2 == 0 and y % 2 == 1) or (x % 2 == 1 and y % 2 == 0))) cout << c++; 
            else cout << " ";
        }
        left--;
        right++;
        cout << endl;
    }
}

